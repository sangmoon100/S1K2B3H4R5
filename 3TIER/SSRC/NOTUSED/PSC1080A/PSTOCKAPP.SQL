CREATE TABLE PSTOCKAPP
(
  EMPNO      VARCHAR2(4 BYTE),
  seq        NUMBER(2),
  korname    VARCHAR2(12 BYTE),
  STOCKNUM   VARCHAR2(2 BYTE),
  appCNT     NUMBER(10),
  cprice     NUMBER(7),
  stockamt   NUMBER(10),
  intax      NUMBER(8),
  jutax      NUMBER(7),
  appemp     VARCHAR2(4 BYTE),
  appdate    VARCHAR2(15 BYTE),
  conemp     VARCHAR2(4 BYTE),
  conDATE    VARCHAR2(15 BYTE),
  payemp     VARCHAR2(4 BYTE),
